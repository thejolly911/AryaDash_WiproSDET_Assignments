//7.Convert null to wrapper classes

package Day_6;

public class Null_wrapper {
    public static void main(String[] args) {

        String str = null;

        Integer num = null;
        Boolean bool = null;

        System.out.println("String: " + str);
        System.out.println("Integer: " + num);
        System.out.println("Boolean: " + bool);
    }
}